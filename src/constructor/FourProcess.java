package constructor;

public class FourProcess {
	double x;
	double y;
	
	public FourProcess() {
		
	}
	
	public double gather(double x, double y) {
		return x + y;
	}
	
	public double subtract(double x, double y) {
		return x - y;
	}
	
	public double multiply(double x, double y) {
		return x * y;
	}
	
	public double divide(double x, double y) {
		return x / y;
	}
}
